class swap
  {
    public static void main(String[]args)
    {
      int e=55;
      int f=99;
      //printing the values of e and f before swapping
      System.out.println("before swapping : e=" +e+ "f=" +f);
      int tempo=e;
      e=f;
      f=tempo;
      //printing the values of e and f after swapping
      System.out.println("after swapping : e=" +e+ "f=" +f);
    }
  }

      
